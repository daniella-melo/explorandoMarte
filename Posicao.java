public class Posicao {
    private int X;
    private int Y;

    public Posicao(int X, int Y){
        this.X = X;
        this.Y = Y;
    }

    public int getX(){
        return this.X;
    }

    public int getY(){
        return this.Y;
    }

    public void setX(int X){
        this.X = X;
    }

    public void setY(int Y){
        this.Y = Y;
    }
}
